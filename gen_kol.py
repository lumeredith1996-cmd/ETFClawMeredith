def gen_feed(kol, idx):
    t = now - idx * random.randint(1800, 14400)
    content = random.choice(FEED_TEMPLATES)
    plat = random.choice(kol['p']) if kol['p'] else '雪球'
    return {
        "kol": kol['name'],
        "rating": kol['r'],
        "platform": plat,
        "content": content,
        "tags": kol['t'][:2] if kol['t'] else [],
        "time": t,
        "note": kol.get('note',''),
        "loc": kol.get('loc',''),
        "coop": kol.get('coop',''),
    }

